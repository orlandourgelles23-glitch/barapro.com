/**
 * Get a translation value by dot-notation key.
 * @param key Dot-notation key (e.g. 'sidebar.panel')
 * @param locale Target locale ('es' | 'en'). Defaults to 'es'.
 * @param fallbackLocale Optional fallback locale if key not found in primary.
 */
export function getTranslation(key: string, locale: Locale = 'es', fallbackLocale?: Locale): string {
  const parts = key.split('.');
  if (parts.length < 2) return key;

  const namespace = parts[0];
  const fieldPath = parts.slice(1);

  // Helper to resolve nested path
  const resolvePath = (obj: Record<string, string | Record<string, string>>, path: string[]): string | undefined => {
    let current: string | Record<string, string> | undefined = obj[path[0]];
    if (current === undefined) return undefined;
    for (let i = 1; i < path.length; i++) {
      if (typeof current !== 'object' || current === null) return undefined;
      current = current[path[i]];
    }
    return typeof current === 'string' ? current : undefined;
  };

  // Try primary locale
  const primaryDict = translations[locale]?.[namespace];
  if (primaryDict) {
    const val = resolvePath(primaryDict, fieldPath);
    if (val !== undefined) return val;
  }

  // Try fallback locale
  if (fallbackLocale && fallbackLocale !== locale) {
    const fallbackDict = translations[fallbackLocale]?.[namespace];
    if (fallbackDict) {
      const val = resolvePath(fallbackDict, fieldPath);
      if (val !== undefined) return val;
    }
  }

  // Try Spanish as ultimate fallback
  if (locale !== 'es') {
    const esDict = translations.es?.[namespace];
    if (esDict) {
      const val = resolvePath(esDict, fieldPath);
      if (val !== undefined) return val;
    }
  }

  // Return key if not found
  return key;
}
